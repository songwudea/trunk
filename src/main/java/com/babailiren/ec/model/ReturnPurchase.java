package com.babailiren.ec.model;

import java.util.Date;

public class ReturnPurchase implements java.io.Serializable {
	
	private static final long serialVersionUID = 1L;

	private Integer id;
	
	private Integer itemId;
	
	// 理由
	private String reson;
	
	// 审核
	private String auditing;
	
	// 发货单号
	private String orderDispatchNumber;
	
	private String remark;

	// 退款状态
	private String status;
	
	private Date buyDate;
	
	// 申请退款时间
	private String createAt;
	
	private Integer price;
	
	private String sellAddress;
	
	private Date returnPurchaseDate;
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getReson() {
		return reson;
	}

	public void setReson(String reson) {
		this.reson = reson;
	}

	public String getAuditing() {
		return auditing;
	}

	public void setAuditing(String auditing) {
		this.auditing = auditing;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getOrderDispatchNumber() {
		return orderDispatchNumber;
	}

	public void setOrderDispatchNumber(String orderDispatchNumber) {
		this.orderDispatchNumber = orderDispatchNumber;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Date getBuyDate() {
		return buyDate;
	}

	public void setBuyDate(Date buyDate) {
		this.buyDate = buyDate;
	}

	public String getCreateAt() {
		return createAt;
	}

	public void setCreateAt(String createAt) {
		this.createAt = createAt;
	}

	public Integer getPrice() {
		return price;
	}

	public void setPrice(Integer price) {
		this.price = price;
	}

	public String getSellAddress() {
		return sellAddress;
	}

	public void setSellAddress(String sellAddress) {
		this.sellAddress = sellAddress;
	}

	public Date getReturnPurchaseDate() {
		return returnPurchaseDate;
	}

	public void setReturnPurchaseDate(Date returnPurchaseDate) {
		this.returnPurchaseDate = returnPurchaseDate;
	}

	public Integer getItemId() {
		return itemId;
	}

	public void setItemId(Integer itemId) {
		this.itemId = itemId;
	}
}
