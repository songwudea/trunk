package com.babailiren.ec.model;

import java.util.Date;

public class Refund {
	
	private Integer id;
	
	// 价格
	private Integer price;
	
	private String orderNumber;
	
	private Integer siteId;
	
	private String createByEmployeeName;
	
	private String confirmByEmployeeName;
	

	// 退款的账号
	private String account;
	
	private String status;
	
	// 退款单生成时间
	private Date createdAt;
	
	// 退款时间
	private Date refundDate;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getPrice() {
		return price;
	}

	public void setPrice(Integer price) {
		this.price = price;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public Date getCreatedAt() {
		return createdAt;
	}

	public void setCreatedAt(Date createdAt) {
		this.createdAt = createdAt;
	}

	public Date getRefundDate() {
		return refundDate;
	}

	public void setRefundDate(Date refundDate) {
		this.refundDate = refundDate;
	}

	public String getOrderNumber() {
		return orderNumber;
	}

	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}

	public Integer getSiteId() {
		return siteId;
	}

	public void setSiteId(Integer siteId) {
		this.siteId = siteId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getCreateByEmployeeName() {
		return createByEmployeeName;
	}

	public void setCreateByEmployeeName(String createByEmplyeeName) {
		this.createByEmployeeName = createByEmplyeeName;
	}

	public String getConfirmByEmployeeName() {
		return confirmByEmployeeName;
	}

	public void setConfirmByEmployeeName(String confirmByEmplyeeName) {
		this.confirmByEmployeeName = confirmByEmplyeeName;
	}
}
