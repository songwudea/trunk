package com.babailiren.ec.web.vo;

public class AttrJson {
	
	private String attr;
	private String attrType;
	private String attrOption;
	private String oldValue;
	private boolean lostAttr;
	
	public String getOldValue() {
		return oldValue;
	}
	public void setOldValue(String oldValue) {
		this.oldValue = oldValue;
	}
	public String getAttr() {
		return attr;
	}
	public void setAttr(String attr) {
		this.attr = attr;
	}
	public String getAttrType() {
		return attrType;
	}
	public void setAttrType(String attrType) {
		this.attrType = attrType;
	}
	public String getAttrOption() {
		return attrOption;
	}
	public void setAttrOption(String attrOption) {
		this.attrOption = attrOption;
	}
	public boolean isLostAttr() {
		return lostAttr;
	}
	public void setLostAttr(boolean lostAttr) {
		this.lostAttr = lostAttr;
	}
}
