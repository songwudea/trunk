package com.babailiren.ec.web.vo;

import java.util.HashMap;
import java.util.List;
import java.util.Map;

import com.babailiren.ec.model.Brand;
import com.babailiren.ec.model.Item;

/**
 * 首页 VO
 */
public class IndexVO {
	// 首页的品牌
	private Map<String, List<Brand>> brands = new HashMap<String, List<Brand>>();
	
	// 首页的商品
	private Map<String, List<Item>> items = new HashMap<String,List<Item>>();

	public Map<String, List<Brand>> getBrands() {
		return brands;
	}

	public void setBrands(Map<String, List<Brand>> brands) {
		this.brands = brands;
	}

	public Map<String, List<Item>> getItems() {
		return items;
	}

	public void setItems(Map<String, List<Item>> items) {
		this.items = items;
	}
}
